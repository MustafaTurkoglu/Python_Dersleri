#Operatörler

"""
Aritmekik İşlem Operatörleri:
-Aritmetik işlemler sırasında kullanıdığımız temel operatörlerdir.

Operatör    İşlev
+           Toplama
-           Çıkarma
*           Çarpma
/           Bölme
//          Tam sayı bölme
%           Mod alma(kalanı hesaplama)
**          Üs alma
"""




"""
İkili işlem Operatörleri:
-İkili işlem(bitwise) operatörleri.

Operatör    İşlev
<<          Sola 1 bit kaydırma
>>          Sağa 1 bit kaydırma
&           AND
|           OR
^           XOR
~           NOT
"""




"""
Mantıksal Operatörler:

Operatör    İşlev
<           Küçüktür
>           Büyüktür
<=          Küçük veya eşit
>=          Büyük veya eşit
==          Eşit
!=          Eşit değil
in          Mevcut
is          Değer ve ID eşitliği
"""




"""
Atama Operatörleri:

Operatör    İşlev
+=          Arttırarak ata
-=          Eksilterek ata
*=          Çarparak ata
/=          Bölerek ata
**=         Üs alarak ata
//=         Tam sayı bölerek ata
%=          Kalanı hesaplayarak ata
<<=         Sola kaydırarak ata
>>=         Sağa kaydırarak ata
&=          AND uygulayarak ata
|=          OR uygulayarak ata
^=          XOR uygulayarak ata
"""
